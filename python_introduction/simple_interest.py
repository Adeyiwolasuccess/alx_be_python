# Define the principal amount
principal = 1000  # representing $1000

# Define the annual interest rate (as a decimal)
rate = 0.05  # representing 5% annual interest rate

# Define the time the money is invested for in years
time = 3  # representing 3 years

# Calculate the simple interest
# Formula: I = P * R * T
interest = principal * rate * time

# Print the calculated interest
# Expected output format: The simple interest is: [interest]
print(f"The simple interest is: {interest}")
